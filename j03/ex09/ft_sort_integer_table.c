void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int r;

	i = 0;
	while (i < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			r = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = r;
			i--;
		}
		else
		{
			i++;
		}
	}
}
